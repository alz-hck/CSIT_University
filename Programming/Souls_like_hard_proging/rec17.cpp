#include <iostream> 
using namespace std; 

// Рекурсивная функция для вывода первой половины таблицы
void firsthalf(int n, int cur = 1) {
    // Основное условие рекурсий всех времён и народов: если текущий номер строки превышает n, завершаем рекурсию
    if (cur > n) return; 
    
    // Выводим текущий номер cur, повторяя его cur раз
    for (int i = 0; i < cur; ++i) {
        cout << cur; 
    }
    
    // Выводим дефисы: 2 * (n - cur) - 1 раз
    for (int i = 0; i < 2 * (n - cur) - 1; ++i) {
        cout << "-"; // Вывод дефисов
    }
    
    // Снова выводим текущее число cur, повторяя его cur раз
    for (int i = 0; i < cur; ++i) {
        cout << cur; 
    }
    
    cout << endl; 
    
    // Рекурсивный вызов для следующей строки с увеличением cur на 1
    firsthalf(n, cur + 1);
}

// Функция для вывода всей таблицы
void pira(int n) {
    firsthalf(n); // Выводим первую половину таблицы
    
    // Выводим вторую половину таблицы в обратном порядке
    for (int i = n - 1; i >= 1; --i) {
        for (int j = 0; j < i; ++j) {
            cout << i; // Вывод текущего числа i
        }
        
        // Выводим дефисы: 2 * (n - i) - 1 раз
        for (int j = 0; j < 2 * (n - i) - 1; ++j) {
            cout << "-"; 
        }
        
        // Снова выводим текущее число i, повторяя его i раз
        for (int j = 0; j < i; ++j) {
            cout << i; 
        }
        
        cout << endl; 
    }
}

int main() {
    int n; 
    cout << "Введите n: "; 
    cin >> n; // Ввод

    pira(n); // Использование функции pira для вывода таблицы

    return 0;
}
